from math import sqrt

x1 = float(input("X1\n"))
y1 = float(input("Y1\n"))
x2 = float(input("X2\n"))
y2 = float(input("Y2\n"))

print("The distance is the square root of", (x2 - x1) ** 2 + (y2 - y1) ** 2, "or", sqrt((x2 - x1) ** 2 + (y2 - y1) ** 2))

