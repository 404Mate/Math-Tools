radius = input("What is your radius?\n")
height  = input("What is your height?\n")
radius = float(radius)
height = float(height)
circumfrence = radius
radius = radius * radius
area = radius * 3.14 
surface = height * 2 * circumfrence * 3.14 + area + area
print("Your surface area is:", surface)

